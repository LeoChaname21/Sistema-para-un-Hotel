    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        if (!txtidpersona.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estas seguro de eliminar el huesped?", "confirmar", 2);

            if (confirmacion == 0) {
                fhuesped func = new fhuesped();
                Huesped hue = new Huesped();

                hue.setIdper(Integer.parseInt(txtidpersona.getText()));
                func.eliminar(hue);
                mostrar("");
                inhabilitar();

            }

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

