    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNombres.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el nombre del huesped");
            txtNombres.requestFocus();
            return;
        }
        if (txtPaterno.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el apellido paterno del huesped");
            txtPaterno.requestFocus();
            return;
        }
        if (txtMaterno.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el apellido materno del huesped");
            txtMaterno.requestFocus();
            return;
        }

        if (txtNdoc.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el número del documento");
            txtNdoc.requestFocus();
            return;
        }
        if (txtCodigo.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el codigo del huesped");
            txtCodigo.requestFocus();
            return;
        }

        Huesped hue = new Huesped();
        fhuesped func = new fhuesped();

        hue.setNom(txtNombres.getText());
        hue.setApep(txtPaterno.getText());
        hue.setApem(txtMaterno.getText());

        int seleccionado = jcbtipodoc.getSelectedIndex();
        hue.setTdoc((String) jcbtipodoc.getItemAt(seleccionado));

        hue.setNdoc(txtNdoc.getText());
        hue.setDir(txtDireccion.getText());
        hue.setCel(txtCelular.getText());
        hue.setCodhuesped(txtCodigo.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(hue)) {
                JOptionPane.showMessageDialog(rootPane, "El huesped fue registrado correctamente");
                mostrar("");
                inhabilitar();

            } else {
                JOptionPane.showMessageDialog(rootPane, "El huesped no fue registrado correctamente");

            }
        } else if (accion.equals("editar")) {
            hue.setIdper(Integer.parseInt(txtidpersona.getText()));

            if (func.editar(hue)) {
                JOptionPane.showMessageDialog(rootPane, "El huesped fue editado correctamente");
                mostrar("");
                inhabilitar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

