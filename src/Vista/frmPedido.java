    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        if (txtCosto.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el costo del pedido");
            txtCosto.requestFocus();
            return;
        }

        if (txtDescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el costo del pedido");
            txtDescripcion.requestFocus();
            return;
        }

        Pedido rsv = new Pedido();
        fpedido func = new fpedido();

        rsv.setIdproveedor(Integer.parseInt(txtidproveedor.getText()));
        rsv.setDescripcion(txtDescripcion.getText());
        rsv.setCosto(Double.parseDouble(txtCosto.getText()));

        int seleccionado = jcbTipo.getSelectedIndex();
        rsv.setTipo((String) jcbTipo.getItemAt(seleccionado));

        Calendar cal;
        int d, m, a, h;
        cal = dcfechapedido.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        rsv.setFechapedido(new Date(a, m, d));

        if (accion.equals("guardar")) {
            if (func.insertar(rsv)) {
                JOptionPane.showMessageDialog(rootPane, "El pedido fue registrada correctamente");
                mostrar("");
                inhabilitar();

            } else {
                JOptionPane.showMessageDialog(rootPane, "El pedido no fue registrada correctamente");

            }
        } else if (accion.equals("editar")) {
            rsv.setIdpedido(Integer.parseInt(txtidpedidos.getText()));

            if (func.editar(rsv)) {
                JOptionPane.showMessageDialog(rootPane, "El pedido fue editado correctamente");
                mostrar("");
                inhabilitar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

